	//randomizer
	function randomize(a) {
		var i;
		for (i = a.length; i--; ) {
			var j = Math.floor((i + 1) * Math.random());
			var temp = a[i];
			a[i] = a[j];
			a[j] = temp;
		}
		return a;
	}
	
	//main
	function leroLero() {
		//get form entries
		var title = document.getElementById("t_titulo").value;
		var lines = document.getElementById("t_linhas").value;

		//new randomized arrays
		var r_tab0 = randomize(tab0);
		var r_tab1 = randomize(tab1);
		var r_tab2 = randomize(tab2);
		var r_tab3 = randomize(tab3);

		//build paragraphs
		var paragraph = 0;
		var text = "";
		while (lines > 0) {
			for (i = 0; i < r_tab0.length; i++) {
				text += r_tab0[i] + r_tab1[i] + r_tab2[i] + r_tab3[i] + " ";
				//randomize between 2 and 5 line paragraph and add break-line
				if (++paragraph >= (2 + Math.ceil(Math.random() * 3))) {
					text += "<br><br>";
					paragraph = 0;
				}
				if (--lines <= 0) {
					break;
				}
			}
		}
		//put a nice title
		document.getElementById("titulo").innerHTML = title;
		//put a wonderful bullshit
		document.getElementById("texto").innerHTML = text;
	}
